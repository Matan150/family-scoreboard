def clean_word(w: str) -> str:
    return w.strip(".,!?;:\"'()[]")


def is_direct_address(text: str, name: str) -> bool:
    text_lower = text.lower()
    name_lower = name.lower()

    idx = text_lower.find(name_lower)
    if idx == -1:
        return False

    before_name = text_lower[:idx].strip()
    after_raw = text_lower[idx + len(name_lower):].strip()
    after_clean = after_raw.lstrip(" .,!?;:")
    words_after = after_clean.split()

    score = 0

    if before_name == "":
        score += 3

    if after_raw in ("", "?", "!"):
        return True  # vocative

    limit = min(4, len(words_after))
    for i in range(limit):
        w = clean_word(words_after[i])
        if w in DIRECT_WORDS:
            score += 2
        if w in INDIRECT_WORDS:
            score -= 3

    return score > 0
